public class Q8<String>{
    /*
    * Generics
    * Generics are parameterized types that allow you to write classes and methods that work with
    * different types of objects. Generics are a way to make your code more flexible and reusable.
    *
    * You can create different classes that work with different data types.
    *
    * Generic Classes
    * Generic classes contains a type parameter section.
    * There can be more than one type paramter, separated by comma
    *
    * */
    private String t;

    public void set(String t) { this.t = t; }
    public String get() { return t; }

}